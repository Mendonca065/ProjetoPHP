<h1>The Odin Workout</h1>

<p>
    The Odin Workout é um site onde disponibilizamos periodizações de treino para iniciantes, intermediários e avançados na musculação,
    nossos treinos são atualizados a cada 3 meses, assim, providenciando novos estimulos trimestralmente.
</p>

<p>
    O nome, The Odin Workout faz uma referencia a um deus da mitologia nordica Odin, e tambem é uma homenagem à <a href="https://www.youtube.com/watch?v=OZWBYxJSuSI" target="_blank">Dennis Roberson</a>
    uma figura muito querida no esporte que veio a óbito devido um infarto enquanto treinava. Descanse em Paz, filho de Odin.
</p>
